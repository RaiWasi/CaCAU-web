package com.airbus.alten.cacau.logic;

public class ParameterToSearchConfiguration {

	private String aircraftModel;
	private String aircraftVersion;
	private String aircraftEngine;
	private String aircraftApu;
	
	public String getAircraftModel() {
		return aircraftModel;
	}
	public void setAircraftModel(String aircraftModel) {
		this.aircraftModel = aircraftModel;
	}
	public String getAircraftVersion() {
		return aircraftVersion;
	}
	public void setAircraftVersion(String aircraftVersion) {
		this.aircraftVersion = aircraftVersion;
	}
	public String getAircraftEngine() {
		return aircraftEngine;
	}
	public void setAircraftEngine(String aircraftEngine) {
		this.aircraftEngine = aircraftEngine;
	}
	public String getAircraftApu() {
		return aircraftApu;
	}
	public void setAircraftApu(String aircraftApu) {
		this.aircraftApu = aircraftApu;
	}

}
